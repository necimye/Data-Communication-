
x = [2 5; 4 -6]
y = [1 5; 6 -2]

## sum
display(x+y)

## Difference
display(x-y)

## product
display(x * y)

## transpose of x
display(x')

## inverse of x
display(inv(x))

## element by element multiplication
display (x .* y)

## element by element division
display (x ./ y)

## square each element of x
display (x .^ 2)

## raising x to y (individual elements)
display(x .^ y)
